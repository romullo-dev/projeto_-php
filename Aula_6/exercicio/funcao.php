<?php
function somar ($n1, $n2)
{
    $resultado = $n1 + $n2;

    return $resultado;
}

function subtrair ($n1, $n2)
{
    $resultado = $n1 - $n2;
    
    return $resultado;
}

function mult ($n1, $n2)
{
    $resultado = $n1 * $n2;
    
    return $resultado;
}

function divisao ($n1, $n2)
{
    $resultado = $n1 + $n2;
    
    return $resultado;
}